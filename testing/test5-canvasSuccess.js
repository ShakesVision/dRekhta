        const canvasWidth = tileSize * parseInt(data.X) || originalWidth;

        const canvas = createCanvas(canvasWidth, canvasHeight);
        // const canvas = createCanvas(originalWidth, originalHeight);
        const ctx = canvas.getContext('2d');
