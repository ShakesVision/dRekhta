async function unscrambleImage(inputPath) {
    const resized = await sharp(inputPath).resize(676, 1080);
    const { PageHeight, PageWidth, Sub } = key;
    const cellSize = 50;
    const borderWidth = 16;

    let target = await sharp({
        create: {
            width: (PageWidth > 0 ? PageWidth : X * cellSize),
            height: (PageHeight > 0 ? PageHeight : Y * cellSize),
            channels: 4,
            background: { r: 0, g: 0, b: 0, alpha: 0 } // transparent background
        }
    });

    for (const sub of Sub) {
        const sourceX = sub.X1 * cellSize + sub.X1 * borderWidth;
        const sourceY = sub.Y1 * cellSize + sub.Y1 * borderWidth;
        const targetX = sub.X2 * cellSize;
        const targetY = sub.Y2 * cellSize;

        // Extract and composite the sub-section
        target = await target.composite([{
            // input: sharpScrambledImage.jpeg(),
            input: resized.jpeg(),
            left: targetX,
            top: targetY,
            blend: 'over',
            gravity: 'northwest',
            cutout: {
                x: sourceX,
                y: sourceY,
                width: cellSize + borderWidth,
                height: cellSize + borderWidth
            }
        }]).toFile('final.jpg');
    }
}
